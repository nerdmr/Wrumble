import { Injectable } from '@angular/core';

@Injectable({
    providedIn: 'root'
})
export class DictionaryService {

    constructor() { }

    getRandomWords(length: number) {
        const indicesSoFar: number[] = [];
        const words = [];

        for (let i = 0; i < length; i++) {
            words.push(this.getRandomWord(length, indicesSoFar));
        }

        return words;
    }

    isWord(word: string) {
        return dictionary[word.length].indexOf(word) >= 0;
    }

    private getRandomWord(length: number, indicesSoFar: number[]): string {

        const randIndex = Math.floor(Math.random() * (dictionary[length].length));

        if (indicesSoFar.indexOf(randIndex) !== -1) {
            return this.getRandomWord(length, indicesSoFar);
        } else {
            indicesSoFar.push(randIndex);
            return dictionary[length][randIndex];
        }
    }
}

